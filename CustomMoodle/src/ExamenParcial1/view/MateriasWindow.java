    private void bUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUpdateActionPerformed
        // TODO add your handling code here:
        String materia = (String) cbMaterias.getSelectedItem();
        String docente = tfDocente.getText();
        Subject subject = subjectBLO.findByName(materia).get(0);
        
        
        List<Student> studentList = studentBLO.findAll();
        
        for(int i = 0; i < studentList.size(); i++)
        {
            List<Subject> subjects = studentList.get(i).getMaterias();
            for(int j = 0; j < subjects.size(); j++)
            {
            if(subjects.get(j).getNombre().equals(subject.getNombre()))
                {
                    subjects.get(j).setDocente(docente);
                }
            }
        }
        
        subject.setDocente(docente);
        subjectBLO.update(subject);
    }//GEN-LAST:event_bUpdateActionPerformed

